//Print a 3*3 matrix from 1 to 9 numbers using forloop or while loop?
//Print a 4*4 matrix from 1 to 16 numbers using forloop or while loop?
public class forLoop3by3Matrix {
	public static void main(String[] args) {
				
		for (int i=1; i<=7; i=i+3){
		System.out.print(i+" ");
		}
		
		System.out.println();
		for (int j=2; j<=8; j=j+3){
		System.out.print(j+" ");
		}
	    
		System.out.println();
     	for (int k=3; k<=9; k=k+3){
		System.out.print(k+" ");
		
     	}
		System.out.println();
	}
		
}	


